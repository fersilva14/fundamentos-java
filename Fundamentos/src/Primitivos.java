
public class Primitivos {

	public static void main(String[] args) {
			//tipos primitivos (variaveis)
			//tipo boleano (verdadeiro ou falso)
			boolean completo = true;
			//tipo char (1 caractere unicode 'aspas simples')
			char sexo = 'F';
			//tipo byte (numeros inteiros)
			byte idade = 127;
			//tipo short (numeros inteiros)
			short numero1 = 16780;
			//padrão para numeros inteiros
			int numero2 = 1968;
			//tipo long (numero inteiro)
			long numero3 = 2147483647;
			//tipo float (numeros não inteiros)
			float nota1 = 5.6f;
			double nota2 = 7.8; // padrão para numeros não inteiros
			//String (cadeia de caracteres) "aspas"s
			String nome = "Sirlene";
			System.out.println(completo);
			System.out.println(sexo);
			System.out.println(idade);
			System.out.println(numero1);
			System.out.println(numero2);
			System.out.println(numero3);
			System.out.println(nota1);
			System.out.println(nota2);
			System.out.println(nome);
	}

}
